package procesare;

public class Situatie {

	private Destinatie destinatie;
	private Masina calator;
	private Avion avion;
	

	public Avion getAvion() {
		return avion;
	}

	public void setAvion(Avion avion) {
		this.avion = avion;
	}

	public Destinatie getDestinatie() {
		return destinatie;
	}

	public void setDestinatie(Destinatie destinatie) {
		this.destinatie = destinatie;
	}

	public Masina getCalator() {
		return calator;
	}

	public void setCalator(Masina calator) {
		this.calator = calator;
	}
	
	
}
